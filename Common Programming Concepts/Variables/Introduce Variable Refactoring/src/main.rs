fn main() {
    const NINETEEN_NINETY_THREE: i32 = 1993;
    let ten_years_ago = NINETEEN_NINETY_THREE - 10;
    let five_years_ago = NINETEEN_NINETY_THREE - 5;
    let one_year_ago = NINETEEN_NINETY_THREE - 1;

    println!("Ten years ago: {}", ten_years_ago);
    println!("Five years ago: {}", five_years_ago);
    println!("One year ago: {}", one_year_ago);
}